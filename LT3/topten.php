<div>9<img src="sandwich.jpg" alt="Sandwich" style="width:100%"> Sandwich - A sandwich is a food typically consisting of vegetables, sliced cheese or meat, placed on or between slices of bread, or more generally any dish wherein bread serves as a container or wrapper for another food type.</div>
